# Strings: são elemendos dentro de aspas
print("Python")

# Int: são números inteiros
print(11)
print(-28)
print(0)

# Float: são números com casas decimais separadas por ponto
print(4.24)
print(-74.1)
print(0.0)

# Bool: são as valores binários de resposta de verdadeiro (True) ou falso (False)
print(True)
print(False)