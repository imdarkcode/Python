# A função print permite mostrar um elemento na tela
print(1234)

# É possível separar os elemntos utilizando vígula
print(12, 34)

# Por padrão os elemento são separados por um espaço, é possível mudar isso com o atributo "sep"
print(12, 34, sep="-")

# Por padrão o print sempre vai terminar com uma quebra de linha, é possível mudar isso com o atributo "end"
print(1234, end="-")
print(5678)